#!/usr/bin/python


###############################################################################
## IMPORTS                                                                   ##
###############################################################################
import os;
import ConfigParser;


###############################################################################
## CLASS                                                                     ##
###############################################################################
class Config:

    """
    ---------------------------------------------------------------------------
    """
    ###########################################################################
    ## ATTRIBUTES                                                            ##
    ###########################################################################
    __data = None;
    __cfg  = None;


    ###########################################################################
    ## SPECIAL METHODS                                                       ##
    ###########################################################################
    ##
    ##
    ## ------------------------------------------------------------------------
    ## @configFile == config file.
    ##
    def __init__(self, confFile):
        self.__data = {};

        self.__cfg = ConfigParser.RawConfigParser();
        self.__cfg.read(confFile);


    ###########################################################################
    ## PUBLIC METHODS                                                        ##
    ###########################################################################
    ##
    ##
    ## ------------------------------------------------------------------------
    ##
    def extract(self):
        ## Obtain all sections in the config files:
        sections = self.__cfg.sections();

        for section in sections:
             self.__data[section] = {};

             ##
             for option in self.__cfg.options(section):
                 self.__data[section][option] = self.__cfg.get(section, option);

        return self.__data;
## End.







###############################################################################
## MAIN                                                                      ##
###############################################################################
if __name__ == "__main__":
    config = Config('config.ini');
    config.extract();
## End.
